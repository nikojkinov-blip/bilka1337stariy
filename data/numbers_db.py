USED_NUMBERS = {"A": [], "B": []}
PENDING_NUMBERS = {"A": [], "B": []}

def get_random_numbers(letter: str, count: int = 20) -> list:
    available = [n for n in ALL_NUMBERS.get(letter, []) if n not in USED_NUMBERS.get(letter, [])]
    random.shuffle(available)
    selected = available[:count]
    PENDING_NUMBERS[letter] = selected
    return selected

def remove_number(letter: str, index: int):
    if letter in PENDING_NUMBERS and index < len(PENDING_NUMBERS[letter]):
        num = PENDING_NUMBERS[letter].pop(index)
        if letter not in USED_NUMBERS:
            USED_NUMBERS[letter] = []
        USED_NUMBERS[letter].append(num)
        return True
    return False

def add_numbers(letter: str, numbers: list):
    if letter not in ALL_NUMBERS:
        ALL_NUMBERS[letter] = []
    ALL_NUMBERS[letter].extend(numbers)